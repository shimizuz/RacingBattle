#include "CBullet.h"

//==============================================================================
// class implementation
//==============================================================================
//------------------------------------------------
// ctor
//------------------------------------------------
CBullet::CBullet()
    : CScene3D(3),
      spd_(CVector(0, 0, 0)) {
}

//------------------------------------------------
// dtor
//------------------------------------------------
CBullet::~CBullet() {
}

//------------------------------------------------
// Init
//------------------------------------------------
void CBullet::Init(void) {
  CScene3D::Init("data/TEXTURE/texture006.tga", 5.0f, 5.0f);
  SetRotate(CVector(-90.0f, 0.0f, 0));
}

//------------------------------------------------
// Uninit
//------------------------------------------------
void CBullet::Uninit(void) {
  CScene3D::Uninit();
}

//------------------------------------------------
// Reset
//------------------------------------------------
void CBullet::Reset(const CVector& pos, const CVector& spd) {
  SetPosition(pos);
  spd_ = spd;
}


//------------------------------------------------
// Update
//------------------------------------------------
void CBullet::Update(void) {
  CVector pos = GetPosition();
  pos += spd_;
  SetPosition(pos);

  CScene3D::Update();
}

//------------------------------------------------
// Draw
//------------------------------------------------
void CBullet::Draw(void) {
  CScene3D::Draw();
}
